/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func inorderTraversal(root *TreeNode) []int {
	ret := make([]int, 0)
	if root == nil {
		return ret
	}
	dfs(root, &ret)
	return ret
}

func dfs(node *TreeNode, ret *[]int) {
	if node == nil {
		return
	}
	dfs(node.Left, ret)
	*ret = append(*ret, node.Val)
	dfs(node.Right, ret)
}

