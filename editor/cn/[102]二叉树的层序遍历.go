/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func levelOrder(root *TreeNode) [][]int {
	if root == nil {
		return nil
	}

	res := make([][]int, 0)
	queue := make([]*TreeNode, 0)
	queue = append(queue, root)

	for len(queue) != 0 {
		sum := len(queue)
		tmp := make([]int, 0)
		for i := 0; i < sum; i++ {
			n := queue[0]
			queue = queue[1:]
			tmp = append(tmp, n.Val)

			if n.Left != nil {
				queue = append(queue, n.Left)
			}

			if n.Right != nil {
				queue = append(queue, n.Right)
			}
		}
		res = append(res, tmp)
	}

	return res
}

