/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func zigzagLevelOrder(root *TreeNode) [][]int {
	if root == nil {
		return nil
	}

	res := make([][]int, 0)
	queue := make([]*TreeNode, 0)
	queue = append(queue, root)

	flag := true
	for len(queue) != 0 {
		sum := len(queue)
		tmp := make([]int, 0)
		for i := 0; i < sum; i++ {
			n := queue[0]
			queue = queue[1:]

			//æ¯å±‚å¤´å°¾æ’å…¥å˜æ¢ å…ˆå°¾æ’, åå¤´æ’
			if flag {
				//å°¾æ’
				tmp = append(tmp, n.Val)
			} else {
				//å¤´æ’
				tmp = append([]int{n.Val}, tmp...)
			}

			if n.Left != nil {
				queue = append(queue, n.Left)
			}
			if n.Right != nil {
				queue = append(queue, n.Right)
			}
		}

		flag = !flag
		res = append(res, tmp)
	}

	return res
}

