/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func maxDepth(root *TreeNode) int {
	if root == nil {
		return 0
	}

	return dfs333(root)
}

func dfs333(root *TreeNode) int {
	if root == nil {
		return 0
	}

	return int(math.Max(float64(dfs333(root.Left)), float64(dfs333(root.Right)))) + 1
}

